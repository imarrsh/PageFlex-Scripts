// VERY HANDY SCRIPT for (indexOf) since it is not natively supported by Pageflex 5.5 / IE
// makes it possible to search an array and return the index of a match!
var indexOf = function(needle) {
    if(typeof Array.prototype.indexOf === 'function') {
        indexOf = Array.prototype.indexOf;
    } else {
        indexOf = function(needle) {
            var i = -1, index = -1;

            for(i = 0; i < this.length; i++) {
                if(this[i] === needle) {
                    index = i;
                    break;
                }
            }

            return index;
        };
    }

    return indexOf.call(this, needle);
};
// common formatting for the rates when returning the arrays
function formatCells (x){
	return x.replace(/\,/g , "").replace(/\s/g , "\n").replace(/\b(?=(\d+\.))/g, "$").replace(/\n\s*/gm, "").replace(/\b(?=(\$))/g, "\n").replace(/(?:\$0\.00)/g, "N/A").replace(/\B(?=(\d{3})+(?!\d))/g, ",").toLocaleString();
	// 
	// 1. replace all commas with nothing, then
	// 2. replace all spaces with a new line, then
	// 3. lookahead and match digits up to a decimal point, insert a '$', then
	// 4. when indices are removed, remove line breaks with no spaces, then
	// 5. lookahead and find a dollar sign and start a new line, then
	// 6. find '$0.00' and replace with 'N/A' (because iterating and finding that value prevented proper line breaks), then
	// 7. lookahead and re-insert commas at the 1000th place when digits are found
	// probably could make this more efficient, but for now 
	// the data winds up exactly as it is supposed to
}

